        const sections = document.querySelectorAll('section');
        const scrollLine = document.querySelector('.scroll-line');
        let isScrolling = false;

        const updateScrollIndicator = () => {
            const totalHeight = document.documentElement.scrollHeight - window.innerHeight;
            const scrollProgress = (window.scrollY / totalHeight) * 100;
            scrollLine.style.width = `${scrollProgress}%`;
        };

        window.addEventListener('scroll', () => {
            updateScrollIndicator();
        });

        // Определяем текущую секцию
        function getCurrentSectionIndex() {
            // Если мы в самом верху страницы, это первая секция
            if (window.scrollY === 0) {
                return 0;
            }

            for (let i = 0; i < sections.length; i++) {
                const section = sections[i];
                const sectionTop = section.offsetTop;
                const sectionBottom = sectionTop + section.offsetHeight;

                // Если мы находимся в пределах секции
                if (window.scrollY >= sectionTop && window.scrollY < sectionBottom) {
                    return i;
                }
            }
            return -1;
        }

        window.addEventListener('wheel', (e) => {
            // Определяем текущую секцию
            const currentSectionIndex = getCurrentSectionIndex();

            // Если мы в третьей секции, позволяем обычный скролл
            if (currentSectionIndex === 2) {
                // Если достигли конца третьей секции и скроллим вниз, переходим на следующую секцию
                const thirdSection = sections[2];
                const thirdSectionBottom = thirdSection.offsetTop + thirdSection.offsetHeight;

                if (window.scrollY + window.innerHeight >= thirdSectionBottom && e.deltaY > 0) {
                    e.preventDefault();
                    sections[3].scrollIntoView({ behavior: 'smooth' });
                    return;
                }

                // Если достигли начала третьей секции и скроллим вверх, переходим на предыдущую секцию
                const thirdSectionTop = thirdSection.offsetTop;

                if (window.scrollY <= thirdSectionTop && e.deltaY < 0) {
                    e.preventDefault();
                    sections[1].scrollIntoView({ behavior: 'smooth' });
                    return;
                }

                return;
            }

            // Для остальных секций применяем автоматический переход
            e.preventDefault();

            if (isScrolling) return;
            isScrolling = true;

            // Специальная обработка для первой секции
            if (currentSectionIndex === 0) {
                if (e.deltaY > 0) {
                    // Скролл вниз с первой секции
                    sections[1].scrollIntoView({ behavior: 'smooth' });
                }

                setTimeout(() => {
                    isScrolling = false;
                }, 1000);
                return;
            }

            let targetSection;
            if (e.deltaY > 0) {
                // Скролл вниз
                targetSection = sections[currentSectionIndex + 1];
            } else {
                // Скролл вверх
                targetSection = sections[currentSectionIndex - 1];
            }

            if (targetSection) {
                if (targetSection === sections[0]) {
                    // Если переход на первый раздел, прокручиваем на самый верх
                    window.scrollTo({ top: 0, behavior: 'smooth' });
                } else {
                    targetSection.scrollIntoView({ behavior: 'smooth' });
                }
            }

            setTimeout(() => {
                isScrolling = false;
            }, 1000); // Задержка для предотвращения множественных скроллов
        }, { passive: false });
    </script>
